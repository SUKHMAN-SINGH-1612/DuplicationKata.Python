from kata.Song import Song


class Lesson1Straight(Song):
    def sing_cat_song(self):
        self.sing("We gave the cat to a little kid")
        def box1():
            self.sing("But the cat came back")
            self.sing("The very next day")
            self.sing("Oh the cat came back")
            self.sing("We thought he was a goner")
            self.sing("But the cat came back, he just wouldn't go away")
        box1()

        self.sing("We sent the cat out on a boat")
        def box2():
            self.sing("But the cat came back")
            self.sing("The very next day")
            self.sing("Oh the cat came back")
            self.sing("We thought he was a goner")
            self.sing("But the cat came back, he just wouldn't go away")
        box2()